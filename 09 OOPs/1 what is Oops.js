// OOps:- progaming paradigm based on the concept of object
// Object-oriented programming (OOP) is a programming paradigm based on the
// concept of objects;
// We use objects to model (describe) real-world or abstract features;
//  Objects may contain data (properties) and code (methods). By using objects, we
// pack data and the corresponding behavior into one block;
// In OOP, objects are self-contained pieces/blocks of code;
// Objects are building blocks of applications, and interact with one another;
//  Interactions happen through a public interface (API): methods that the code
// outside of the object can access and use to communicate with the object;
// OOP was developed with the goal of organizing code, to make it more flexible
// and easier to maintain (avoid “spaghetti code”)

// class:-
// Like a blueprint from which we can create new objects
// Abstraction:- Abstraction: Ignoring or hiding details that don’t matter, allowing us to get an overview perspective of the thing we’re implementing, instead of messing with details that don’t really matter to our implementation

// Polymorphism:-Polymorphism: A child class can overwrite a method it inherited from a parent class [it’s more complex that that, but enough for our purposes].

// Inheritance :- Inheritance: Making all properties and methods of a certain class available to a child class, forming a hierarchical relationship between classes. This allows us to reuse common logic and to model real-world relationships

// Encapsulation:- Encapsulation: Keeping properties and methods private inside the class, so they are not accessible from outside the class. Some methods can be exposed as a public interface (API).

